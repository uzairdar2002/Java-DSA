package Loops;

/**
 * WhileLoop
 * 
 * It a an entry loop.
 * It checks the condition first the the loop body is executed.
 * Checks condition again and again until true.
 */
public class WhileLoop {

    public static void main(String[] args) {
        int count = 0;

        while (count <= 10) {
            System.out.println("I am learning Loops in Java.");
            count++;
        }

    }
}