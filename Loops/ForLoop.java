package Loops;

/*
 * For Loop.
 * It is also an entry loop. Initilization part executes only once where as condition part, body part and incre/decre part executes again & again until the condition is satisfied.
 */
public class ForLoop {
    public static void main(String[] args) {
        for (int i = 0; i <= 10; i++) {
            System.out.println("Hello Viewers!");
        }
    }
}
