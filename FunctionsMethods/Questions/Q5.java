package FunctionsMethods.Questions;

public class Q5 {
    public static void main(String[] args) {
        int a = 4;
        int b = 8;

        System.out.println("Used to get minimum:  " + Math.min(a, b));
        System.out.println("Used to get maximum:  " + Math.max(a, b));
        System.out.println("Used to calculate absolute value: " + Math.abs(a));
        System.out.println("Used to calculate a number raise to the power of another number:  " + Math.pow(a, b));
        System.out
                .println("Used to return a double value which is a square root of the given number:  " + Math.sqrt(b));
    }
}
